import UIKit

extension UIFont {
    static var bodyB1SemiboldStyle: UIFont {
        return UIFont.systemFont(ofSize: 16, weight: .semibold)
    }

    static var captionC1Style: UIFont {
        return UIFont.systemFont(ofSize: 12, weight: .regular)
    }

    static var ntCaptionStyle: UIFont {
        return UIFont.systemFont(ofSize: 10, weight: .regular)
    }

    static var ntSubtitle2Style: UIFont {
        return UIFont.systemFont(ofSize: 14, weight: .semibold)
    }

    static var bodyB1Style: UIFont {
        return UIFont.systemFont(ofSize: 16, weight: .bold)
    }

    static var bodyB2Style: UIFont {
        return UIFont.systemFont(ofSize: 14, weight: .regular)
    }
}
